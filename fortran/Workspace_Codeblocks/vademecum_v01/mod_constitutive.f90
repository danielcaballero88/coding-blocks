    ! ==========================================================================
    FUNCTION linearSimple(rT_C, r_inParams) RESULT(rT_S)
        ! ----------------------------------------------------------------------
        ! DESCRIPTION:
        ! Ley inventada solo para testear el input/output
        ! no espero que tenga sentido en un material real
        ! uso formato reducido porque son tensores de 2x2 simetricos
        ! C = [C11, C22, C12]
        ! S = [S11, S22, S12]
        ! ----------------------------------------------------------------------
        IMPLICIT NONE
        ! ----------------------------------------------------------------------
        REAL(8), INTENT(IN) :: rT_C(3), r_inParams(3)
        ! ----------------------------------------------------------------------
        REAL(8) :: rT_S(3)
        ! ----------------------------------------------------------------------
        REAL(8) :: r_E11, r_E22, r_E12
        ! ----------------------------------------------------------------------

        ! ______________________________________________________________________
        !
        r_E11 = r_inParams(1)
        r_E22 = r_inParams(2)
        r_E12 = r_inParams(3)
        rT_S(1) = r_E11*(rT_C(1)-1.0d0)
        rT_S(2) = r_E22*(rT_C(2)-1.0d0)
        rT_S(3) = r_E12*rT_C(3)
        ! ______________________________________________________________________

        ! ----------------------------------------------------------------------
    END FUNCTION linearSimple
